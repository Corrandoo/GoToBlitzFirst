package me.corrandoo;

public class Step {
    private int stepId;
    private int stepCost;

    public Step(int stepId, int stepCost) {
        this.stepId = stepId;
        this.stepCost = stepCost;
    }

    public int getStepId() {
        return stepId;
    }

    public int getStepCost() {
        return stepCost;
    }
    ///
}
